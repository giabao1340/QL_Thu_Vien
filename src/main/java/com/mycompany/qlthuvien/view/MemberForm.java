    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        String srch = searchTXT.getText();
        DatabaseConnection dbConnection = DatabaseConnection.getInstance();;
        Connection connection = dbConnection.getConnection();
        String sql = "Select * from DocGia where MaDocGia like N'%" + srch + "%' or HoTen like N'%" + srch + "%' ";
        try (PreparedStatement ps = connection.prepareStatement(sql)) {
            ResultSet rs = ps.executeQuery();
            //  jMemTable.setModel(DbUtils.resultSetToTableModel(rs));
            // Create a DefaultTableModel
            DefaultTableModel tableModel = new DefaultTableModel();
            ResultSetMetaData metaData = rs.getMetaData();
            System.out.println("");
            // Add column names
            int columnCount = metaData.getColumnCount();
            for (int i = 1; i < columnCount; i++) {
                tableModel.addColumn(metaData.getColumnName(i));
            }
            // Add rows
            Object[] row = new Object[columnCount];
            while (rs.next()) {
                for (int i = 1; i <= columnCount; i++) {
                    row[i - 1] = rs.getObject(i);
                }
                tableModel.addRow(row);
            }
            jMemTable.setModel(tableModel);
            // Set the model

            System.out.println("Tim thanh cong");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_searchBtnActionPerformed

