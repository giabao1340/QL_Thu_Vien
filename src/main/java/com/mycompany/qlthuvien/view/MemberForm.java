    private void jMemTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMemTableMouseClicked
        // TODO add your handling code here:
        int j = jMemTable.getSelectedRow();
        TableModel model = jMemTable.getModel();
        maMemTXT.setText(model.getValueAt(j, 0).toString());
        tenMemTXT.setText(model.getValueAt(j, 1).toString());
        genderMemTXT.setText(model.getValueAt(j, 7).toString());
        String maDocGia = model.getValueAt(j, 0).toString();
        DatabaseConnection dbConnection = DatabaseConnection.getInstance();
        Connection connection = dbConnection.getConnection();
        String sql = "SELECT Hinh FROM DocGia WHERE MaDocGia = ?";
        try (PreparedStatement ps = connection.prepareStatement(sql)) {
            ps.setString(1, maDocGia);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                byte[] img = rs.getBytes("Hinh");
                if (img != null) {
                    ImageIcon image = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(memAva.getWidth(), memAva.getHeight(), Image.SCALE_SMOOTH));
                    memAva.setIcon(image);
                    System.out.println("Image loaded for MaDocGia: " + maDocGia);
                } else {
                    memAva.setIcon(null);
                    System.out.println("No image found for MaDocGia: " + maDocGia);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jMemTableMouseClicked

